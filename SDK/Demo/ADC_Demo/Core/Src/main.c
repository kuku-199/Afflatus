void ADC_TEST(void)
{
	for(i=0;i<(sizeof(ADC_DR_VALUE)/sizeof(uint32_t));i++)
	{
	  HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1,100);
		ADC_DR_VALUE[i] = HAL_ADC_GetValue(&hadc1);
	}
	HAL_ADC_Stop(&hadc1);
	VBUS=((ADC_DR_VALUE[0] * 3300) / 4096)*(6);// 240.2/40.2
	VREF=__LL_ADC_CALC_VREFANALOG_VOLTAGE(ADC_DR_VALUE[1],LL_ADC_RESOLUTION_12B);
	TEMP=__HAL_ADC_CALC_TEMPERATURE(VREF,ADC_DR_VALUE[2],ADC_RESOLUTION_12B);
	printf("VBUS:%d mV ,VREF:%d mV ,TEMP:%d C \r\n",VBUS,VREF,TEMP);
}
